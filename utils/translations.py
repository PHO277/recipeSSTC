def get_translation(key, language='zh'):
    """获取指定语言的翻译"""
    lang_data = LANGUAGES.get(language, LANGUAGES['zh'])
    return lang_data['translations'].get(key, key)

def get_language_name(language_code):
    """获取语言名称"""
    return LANGUAGES.get(language_code, LANGUAGES['zh'])['name']

def get_all_languages():
    """获取所有支持的语言"""
    return {code: data['name'] for code, data in LANGUAGES.items()}
